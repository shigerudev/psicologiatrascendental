package org.psicologiatrascendental.domain;

/**
 * Las 5 fases del Modelo de Integración Trascendental (MIT).
 * Determinan el tono narrativo y la profundidad del juego.
 */
public enum MitPhase {
    RECONOCIMIENTO,   // Identificación de patrones
    CONFRONTACION,    // Enfrentamiento con la sombra
    INTEGRACION,     // Síntesis de polaridades
    TRASCENDENCIA,   // Elevación meta-cognitiva
    MANIFESTACION    // Expresión en la acción
}
