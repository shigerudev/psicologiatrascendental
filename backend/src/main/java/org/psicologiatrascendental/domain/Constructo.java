package org.psicologiatrascendental.domain;

/**
 * Los 7 constructos de la Psicología Trascendental (Arias).
 * Cada constructo mapea a escalas psicométricas en el juego.
 */
public enum Constructo {
    CONCIENCIA_TRASCENDENTAL,  // I - Despertar interior
    PROPOSITO_TRASCENDENTAL,   // II - Dirección del crecimiento
    SENTIDO_DE_VIDA,          // III - Coherencia existencial
    ENERGIA_PSIQUICA,         // IV - Vitalidad y equilibrio
    ARQUETIPO,                // V - Guía simbólica
    INTEGRACION,              // VI - Unidad del ser
    TRASCENDENCIA             // VII - Conexión con la totalidad
}
